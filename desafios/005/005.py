n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print(f"A soma é {s:.3f}")
print(f"A multiplicação é {m:.3f}")
print(f"A divisão é {d:.3f}")
print(f"A divisão inteira é {di:.3f}")
print(f"A potência é {e:.3f}")