frase = "Curso em Vídeo Python"
print(frase[9])  # Imprime o caractere na posição 9
print(frase[9:14])  # Imprime os caracteres da posição 9 até a posição 13
print(frase[9:21])  # Imprime os caracteres da posição 9 até a posição 20
print(frase[9:21:2])  # Imprime os caracteres da posição    9 até a posição 20, pulando de 2 em 2
print(frase.count("o"))  # Conta quantas vezes a letra "o" aparece na frase
print(len(frase))  # Imprime o comprimento da frase